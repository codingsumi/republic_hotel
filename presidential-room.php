        $(document).ready(function() {
            $('button').click(function() {
                let name = $('#name').val()
                let mobile = $('#mobile').val()
                let email = $('#email').val()
                let check_in = $('#check_in').val()
                let check_out = $('#check_out').val()
                let room_type = $('#room_type').val()
                let price = $('#price').val()

                if (name == "" || email == "" || mobile == "" || check_in == "" || check_out == "") {
                    $('#err').html("All (*) Fields Are Required");
                    setTimeout(function() {
                        $('#err').fadeOut('slow');
                    }, 5000);
                    $('input,textarea').css('border-color', 'red');
                    setTimeout(function() {
                        $('input, textarea').css('border-color', 'grey');
                    }, 5000);
                } else {
                    $('#err').html("");
                    $.ajax({
                        url: "php_assets/book_room.php",
                        type: "post",
                        data: {
                            name: name,
                            mobile: mobile,
                            email: email,
                            check_in: check_in,
                            check_out: check_out,
                            room_type: room_type,
                            price: price
                        },
                        success: function(data) {
                            $('form').trigger("reset");
                            $('#msg').html(data);
                            // setTimeout(function() {
                            //     $('#msg').fadeOut('slow');
                            // }, 5000);
                        }
                    });
                }
            });
        });
